
export const SET_LOADING = 'SET_LOADING';
export const GET_USER = 'GET_USER';

export function userReducer(state, action) {
    const { type, payload } = action;
    switch(type) {
        case SET_LOADING:
            return {
                ...state,
                loading: true
            }
        case GET_USER:
            return {
                ...state,
                user: payload,
                loading: false
            }
        default:
            throw("bro dude, not an action, dawg!?!?!?!?!?!?!??!????????????????!?!!!!!!!!!!!!!!!!!!!!!!?????????????????!??????????????!!!!!!!!!!!!!!!!!!!??????????????!")
    }
}
